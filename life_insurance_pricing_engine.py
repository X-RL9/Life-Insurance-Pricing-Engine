def summation(f, lower, upper):
    return sum(f(k) for k in range(lower, upper + 1))

def simpson(f, lower, upper, n=10):

    if n % 2 != 0:
        n += 1

    h = (upper - lower) / n

    total = f(lower) + f(upper)

    for k in range(1, n):
        if k % 2 == 0:
            total += 2 * f(lower + k * h)
        else:
            total += 4 * f(lower + k * h)

    return (h / 3) * total

def differentiate(expression: str, variable: str = 'x', order: int = 1):
    """
    Differentiate an expression.

    Args:
        expression: Math expression as a string, e.g. 'x**3 + 2*x'
        variable:   Variable to differentiate with respect to (default: 'x')
        order:      Order of differentiation (default: 1)

    Returns:
        Symbolic derivative
    """
    var = sp.Symbol(variable)
    expr = sp.sympify(expression)
    result = sp.diff(expr, var, order)
    return result

