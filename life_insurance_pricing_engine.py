# Force of mortality

def mu(x, t, table):
    age = int(x + t)
    age = min(age, max(table) - 1)

    l_prev = l(age - 1, table) if age > min(table) else l(age, table)
    l_curr = l(age, table)
    l_next = l(age + 1, table)

    if l_curr == 0:
        return 0.0

    return (l_prev - l_next) / (2 * l_curr)

